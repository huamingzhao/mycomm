                <h2>扣除积分</h2>
                <p class="text pd_5">会员的等级根据会员的累积积分进行升级，此处的累积积分是指用户通过各种途径增加的积分总值，并不包含用户使用掉的积分。如：用户通过充值等途径共获取1000积分，且消耗掉400积分，则用户的累积积分为1000，用户现有的积分为1000-600=400积分</p>
                <p class="text">如用户达到17级，可以享受购买名片服务的7折优惠，用户需要购买1000元的服务时，需要的支付的现金为:0.7折*1000=700元，需要扣除的积分为：（1-0.7）*1000*100=30000积分。如此时用户只有20000积分值，则剩余不足的的10000积分值需要使用现金去补足：10000/100=100元。</p>

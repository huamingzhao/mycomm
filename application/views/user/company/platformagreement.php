				<dt>一、 定义及解释</dt>
				<dd>1、上海通路快建网络服务外包有限公司：以下简称“通路快建”或“甲方”。</dd>
				<dd>2、一句话或一句话平台：指网站www.yjh.com</dd>
				<dd>3、企业用户：指符合本合同规定的条件向通路快建支付服务费用，使用“一句话平台服务”的单位或个人（以下简称“乙方”）。</dd>
				<dd>4、投资者：指通过一句话平台注册、浏览、搜索等项目信息的单位或个人；</dd>
				<dd>5、投资考察会：指乙方企业因招商需要而在一句话平台上发布的邀请投资者实地考察沟通的相关活动信息；</dd>
				<dd>6、平台服务费：是指用于购买通路快建提供的一句话平台服务账户管理、技术维护及线下维护服务等。专业服务费为每年RMB3000元（大写：叁仟元整）。</dd>
				<dd>7、赠送金额：指用户充值时，按照通路快建一句话平台收费标准额外赠予客户账户的金额，以下所有情况出现的退费或合同终止情况中，均不予以退还赠送金额；</dd>
				<dd>8、“一句话平台服务”： 指通路快建已经开通的在www.yjh.com 网站相关页面的特定位置展示乙方的项目信息的一句话平台服务及其他相关的衍生服务，包括但不限于：投资者信息搜索、投资者信息查看等服务方式。 </dd>
				<dd>9、“招商通”的预付款：“招商通”是乙方为购买一句话平台服务，在通路快建预存的支付将来发生的费用 ，“招商通”预付款具体数额以单笔《通路快建一句话平台服务订单》为准。</dd>
				<dd> 10、“招商通”的续费：乙方为继续参加“通路快建一句话平台服务” ，在支付前一款规定的首次“招商通”预付款后，后续向通路快建支付且通路快建也同意收取的“招商通”的预付款，每笔支付金额最低RMB10000元（大写：壹万元整） ，不设上限。乙方在每次续费前有义务再次认真阅读并确保完全理解当时的《通路快建一句话平台服务合同》 ，在全部同意本合同所有条款后进行续费，乙方每次续费行为将视为已阅读并完全理解本合同最新版且承诺遵守约定。</dd>

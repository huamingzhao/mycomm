				<dd>3、乙方与通路快建签订的《通路快建一句话平台服务订单》或其他补充协议与通路快建在www.yjh.com网站上公布的《通路快建一句话平台服务合同》有不一致之处的，以《通路快建一句话平台服务合同》为准。</dd>

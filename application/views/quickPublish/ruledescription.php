		<p>主题不明或描述不清，可能会引起读者过多猜测的信息。</p>
		<p>无法确定意图或影响用户浏览的信息，如：输入无意义的文字、符号等。</p>
		<p class="fzcolblue">处理办法：</p>
		<p>不通过；删除；累犯者手机号、IP冻结；情况恶劣的手机号黑名单</p>
		<p><strong>2、灌水信息</strong></p>
		<p class="fzcolred">判定标准：</p>
		<p>同一用户注册多个账户发布大量相同性质的信息，则视为灌水信息。</p>
		<p class="fzcolblue">处理办法：</p>
		<p>删除；累犯者手机号、IP冻结；情况恶劣的手机号黑名单</p>
		<p><strong>3、重复信息</strong></p>
		<p class="fzcolred">判定标准：</p>
		<p>1、同一账户或同一手机在发布的多条信息中，只变动对同一事、物的描述或根本没有任何变动的信息。</p>

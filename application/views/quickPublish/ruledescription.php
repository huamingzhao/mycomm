		<h3>审核信息的原因判定及处理方式</h3>
		<p><strong>1、无意义信息</strong></p>
		<p class="fzcolred">判定标准：</p>
		<p>在标题和内容里堆积关键词，严重影响用户浏览体验的信息。</p>

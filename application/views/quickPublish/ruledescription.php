		<p>非法信息是指涉嫌欺诈或违反社会道德规范或法律法规的信息。一句话审核人员如发现此类信息可以在不事先通知的情况下删除并要求发布者澄清事实，必要时一句话公司将会全力协助公安机关进行查处。</p>
		<p class="fzcolred">判定标准：</p>
		<p>1、侮辱性语言信息：粗口或侮辱、恐吓、中伤他人、庸俗淫秽的词句。</p>
		<p>2、侵犯他人隐私。如在未经过当事人同意的情况下，利用他人的联系方式（电话、QQ、MSN、住宅地址等等）进行信息发布的行为。</p>
		<p>3、冒充他人、商品来源非法、刻意隐瞒事实、欺诈等行为的信息。</p>

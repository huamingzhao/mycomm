		<p>凡是违反了《一句话信息质量控制规定》的信息，一律删除，同时扣除的相应积分数。审核不通过表示发布者有修改并继续提交的权利。</p>
		<p><strong>3、冻结、黑名单、禁止</strong></p>
		<p>冻结：手机、IP冻结</p>
		<p>手机、IP冻结是指限制手机号、IP在<var>30天</var>内在一句话的合法使用权。在冻结期间不能发布信息和评论，同时扣除的相应积分数。</p>
		<p>黑名单：手机黑名单。</p>
		<p>手机黑名单是指限制手机号<var>永久</var>在一句话的合法使用权，同时扣除的相应积分数。</p>
		<p>禁止：账号禁止。</p>
		<p>账号禁止是指限制账号、手机号<var>永久</var>在一句话的合法使用权，同时扣除的相应积分数。</p>
